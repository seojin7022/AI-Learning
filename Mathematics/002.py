# 2 Vector Operation
import numpy as np

# Creating two vector
a = np.array([1, 2, 3, 4, 5])
b = np.array([1, 2, 3, 4, 5])

# adding two vector
add = a + b
print(add)

# Vector subtraction

sub = a - b
print(sub)


# Vector Multiplication
mul = a * b
print(mul)

# Vector Division
div = a / b
print(div)
